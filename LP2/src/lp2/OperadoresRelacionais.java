public class OperadoresRelacionais {
    public static void main(String[] args) {
        //Operadores: == != >= > <= <
        int num1 = 97;
        int num2 = 'a';
        //Igual de comparação
        System.out.println(num1 == num2);
        //Desigualdade
        System.out.println(3 != 3);
        //Maior ou igual
        System.out.println(3 >= 2);
        //Maior
        System.out.println(3 > 2);
        //Menor ou igual
        System.out.println(3 <= 3);
        //Menor
        System.out.println(3 < 4);
    }
}
